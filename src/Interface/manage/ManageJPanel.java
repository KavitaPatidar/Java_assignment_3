    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int row = tblMngCatalog.getSelectedRow();
        if(row<0) {
            JOptionPane.showMessageDialog(null, "Please select a row from the table!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Airplane cus_tomer = (Airplane) air_liner.getCd().getCustomer().get(tblMngCatalog.convertRowIndexToModel(row));//tblMngCatalog.getModel().getValueAt(row, 0);
        air_liner.getCd().deleteCustomer(cus_tomer);
        populate();
    }//GEN-LAST:event_btnDeleteActionPerformed

