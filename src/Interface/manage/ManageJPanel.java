     public void populate() {
        int rowCount = tblMngCatalog.getRowCount();
        DefaultTableModel model = (DefaultTableModel)tblMngCatalog.getModel();
        for(int i = rowCount - 1; i >=0; i--) {
            model.removeRow(i);
        }
        
        for(Airplane cus_tomer: air_liner.getCd().getCustomer()) {
            Object row[] = new Object[5];
            row[0] = cus_tomer.getDepartLocation();
            row[1]= cus_tomer.getArrLocation();
            row[2]=cus_tomer.getDate();
            row[3]=cus_tomer.getFlightNo();
            row[4]=cus_tomer.getPrefTime();
            model.addRow(row);
        }
     }

